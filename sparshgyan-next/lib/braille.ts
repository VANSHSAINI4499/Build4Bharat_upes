export const brailleMap: Record<string, string> = {
  a: 'таБ', b: 'таГ', c: 'таЙ', d: 'таЩ', e: 'таС', f: 'таЛ', g: 'таЫ', h: 'таУ',
  i: 'таК', j: 'таЪ', k: 'таЕ', l: 'таЗ', m: 'таН', n: 'таЭ', o: 'таХ', p: 'таП',
  q: 'таЯ', r: 'таЧ', s: 'таО', t: 'таЮ', u: 'тае', v: 'таз', w: 'та║', x: 'тан',
  y: 'та╜', z: 'та╡', ' ': ' ',
  '0': 'та┤', '1': 'таВ', '2': 'таЖ', '3': 'таТ', '4': 'та▓',
  '5': 'тав', '6': 'таЦ', '7': 'та╢', '8': 'таж', '9': 'таФ',
  '.': 'та▓', ',': 'таВ', '!': 'таЦ', '?': 'таж', '-': 'тад',
}

export const toBraille = (text: string): string =>
  text
    .toLowerCase()
    .split('')
    .map((ch) => brailleMap[ch] ?? ch)
    .join('')
